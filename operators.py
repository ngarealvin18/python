nambari = 4
nambari2 = 8

# mathematical operator
print(nambari2+nambari)
print(nambari2-nambari)
print(nambari2*nambari)
print(nambari2/nambari)
print(nambari2 % nambari)

# comparison operators
print(f"{nambari} is equal to {nambari2}: {nambari == nambari2}")
print(f"{nambari} is greater than {nambari2}: {nambari > nambari2}")
print(f"{nambari} is less than {nambari2}: {nambari < nambari2}")
print(f"{nambari} is greater than or equal to {nambari2}: {nambari >= nambari2}")
print(f"{nambari} is less than or equal to {nambari2}: {nambari <= nambari2}")
print(f"{nambari} is not equal to {nambari2}: {nambari != nambari2}")

# logical operator
num1 = 15
